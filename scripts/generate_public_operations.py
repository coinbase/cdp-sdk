#!/usr/bin/env python3
"""Generate per-language lookup tables of "public" (unauthenticated) OpenAPI operations.

This script reads openapi.yaml, resolves the *effective* security requirement for every
operation (operation-level `security` override, falling back to the document-level default),
and flags an operation as "public" if it can be called without any credentials, i.e. its
effective security is empty (`[]`) or includes an alternative that is exactly
`unauthenticated: []`.

The generated lookup tables let each SDK's auth layer decide, per-request, whether to attach
CDP API credentials -- driven entirely by openapi.yaml, so any future unauthenticated endpoint
works automatically the next time the client is regenerated, without further SDK code changes.

Usage:
    python3 scripts/generate_public_operations.py [path/to/openapi.yaml] [--language LANG]

Where LANG is one of: typescript, python, go, all (default: all). Each SDK's client-generation
command targets only its own language so it doesn't rewrite the other languages' outputs.
"""

from __future__ import annotations

import argparse
import json
import re
from dataclasses import dataclass
from pathlib import Path

import yaml

UNAUTHENTICATED_SCHEME = "unauthenticated"

HTTP_METHODS = {"get", "put", "post", "delete", "options", "head", "patch", "trace"}

REPO_ROOT = Path(__file__).resolve().parent.parent
DEFAULT_SPEC_PATH = REPO_ROOT / "openapi.yaml"

TS_OUTPUT_PATH = (
    REPO_ROOT
    / "typescript"
    / "packages"
    / "cdp-sdk"
    / "src"
    / "openapi-client"
    / "publicOperations.gen.ts"
)
PYTHON_OUTPUT_PATH = REPO_ROOT / "python" / "cdp" / "openapi_client" / "public_operations.py"
GO_OUTPUT_PATH = REPO_ROOT / "go" / "openapi" / "public_operations.gen.go"

GENERATED_NOTICE = (
    "Generated by scripts/generate_public_operations.py from openapi.yaml. Do not edit manually."
)


@dataclass(frozen=True)
class PublicOperation:
    """A single public (unauthenticated) operation, as declared in openapi.yaml."""

    method: str
    path: str

    @property
    def path_regex_source(self) -> str:
        """Convert an OpenAPI path template (e.g. `/foo/{id}`) into a regex source string.

        Deliberately anchored only at the end (`$`), not the start (`^`): the base path
        configured on each SDK's HTTP client (e.g. `https://api.cdp.coinbase.com/platform`)
        is prepended to the OpenAPI operation path before the auth layer sees the resolved
        request path, so an exact full-string match would never succeed. This mirrors the
        existing (hand-written) wallet-auth path matching convention used elsewhere in these
        SDKs, which also matches on a path suffix/substring rather than the full path.
        """
        segments = re.split(r"(\{[^}]+\})", self.path)
        pattern_parts = [
            "[^/]+" if segment.startswith("{") and segment.endswith("}") else re.escape(segment)
            for segment in segments
        ]
        return "".join(pattern_parts) + "$"


def is_unauthenticated_alternative(security_requirement: dict) -> bool:
    """Return True if a single OpenAPI `security` requirement entry is the unauthenticated scheme."""
    return security_requirement == {UNAUTHENTICATED_SCHEME: []}


def is_public_security(effective_security: list) -> bool:
    """Determine if an effective `security` list allows unauthenticated access.

    An operation is public if it has no security requirement at all (`[]`), or if any one of
    its alternative requirements (OpenAPI `security` entries are OR'd) is the bare
    `unauthenticated: []` scheme.
    """
    if effective_security == []:
        return True
    return any(is_unauthenticated_alternative(requirement) for requirement in effective_security)


def load_public_operations(spec_path: Path) -> list[PublicOperation]:
    """Parse openapi.yaml and return the sorted list of public operations."""
    with open(spec_path) as f:
        spec = yaml.safe_load(f)

    # Fail closed: this table gates whether SDKs attach credentials, so an empty or missing
    # document-level `security` (which would cascade to every operation lacking its own
    # override and mark the entire API public) is treated as an error rather than silently
    # disabling auth everywhere. A spec that genuinely needs no default auth is not something
    # this generator should quietly accept.
    default_security = spec.get("security")
    if not default_security:
        raise ValueError(
            f"{spec_path} has no top-level `security` requirement. Refusing to generate "
            "public-operation tables, because every operation without its own `security` "
            "override would be treated as public and the SDKs would stop sending credentials. "
            "Ensure the spec declares a default `security` scheme."
        )

    public_operations: list[PublicOperation] = []

    for path, path_item in (spec.get("paths") or {}).items():
        if not isinstance(path_item, dict):
            continue
        for method, operation in path_item.items():
            if method not in HTTP_METHODS or not isinstance(operation, dict):
                continue
            # An operation is only public when it explicitly overrides `security` (including an
            # explicit empty `[]`). A missing key, or a malformed null value, falls back to the
            # authenticated document default -- fail closed rather than open.
            operation_security = operation.get("security")
            effective_security = (
                default_security if operation_security is None else operation_security
            )
            if is_public_security(effective_security):
                public_operations.append(PublicOperation(method=method.upper(), path=path))

    return sorted(public_operations, key=lambda op: (op.path, op.method))


def render_typescript(operations: list[PublicOperation]) -> str:
    """Render the TypeScript `publicOperations.gen.ts` lookup table source."""
    # A `new RegExp(...)` call (rather than a `/.../ ` literal) sidesteps the need to escape
    # the literal `/` characters that OpenAPI paths always contain.
    entries = "\n".join(
        f'  {{ method: "{op.method}", pathPattern: new RegExp({json.dumps(op.path_regex_source)}) }},'
        for op in operations
    )
    return f"""/**
 * {GENERATED_NOTICE}
 */

/** A single public (unauthenticated) OpenAPI operation. */
export interface PublicOperation {{
  method: string;
  pathPattern: RegExp;
}}

/** Operations that may be called without CDP API credentials, per openapi.yaml. */
export const PUBLIC_OPERATIONS: PublicOperation[] = [
{entries}
];

/**
 * Returns true if the given HTTP method and path correspond to a public (unauthenticated)
 * operation, as declared by the `security` field in openapi.yaml.
 *
 * @param method - The HTTP method of the request (e.g. "GET").
 * @param path - The URL path of the request (e.g. "/v2/x402/discovery/search").
 * @returns True if the operation does not require CDP API credentials.
 */
export function isPublicOperation(method: string, path: string): boolean {{
  const normalizedMethod = method.toUpperCase();
  return PUBLIC_OPERATIONS.some(
    operation => operation.method === normalizedMethod && operation.pathPattern.test(path),
  );
}}
"""


def render_python(operations: list[PublicOperation]) -> str:
    """Render the Python `public_operations.py` lookup table source."""
    entries = "\n".join(
        f'    PublicOperation(method="{op.method}", '
        f'path_pattern=re.compile(r"{op.path_regex_source}")),'
        for op in operations
    )
    return f'''"""{GENERATED_NOTICE}"""

import re
from dataclasses import dataclass


@dataclass(frozen=True)
class PublicOperation:
    """A single public (unauthenticated) OpenAPI operation."""

    method: str
    path_pattern: re.Pattern


PUBLIC_OPERATIONS: list[PublicOperation] = [
{entries}
]


def is_public_operation(method: str, path: str) -> bool:
    """Return True if the operation is public (unauthenticated).

    Determined by the given HTTP method and path, as declared by the `security` field in
    openapi.yaml.

    Args:
        method: The HTTP method of the request (e.g. "GET").
        path: The URL path of the request (e.g. "/v2/x402/discovery/search").

    Returns:
        True if the operation does not require CDP API credentials.

    """
    normalized_method = method.upper()
    return any(
        operation.method == normalized_method and operation.path_pattern.search(path)
        for operation in PUBLIC_OPERATIONS
    )
'''


def render_go(operations: list[PublicOperation]) -> str:
    """Render the Go `public_operations.gen.go` lookup table source."""
    entries = "\n".join(
        f'\t{{Method: "{op.method}", PathPattern: regexp.MustCompile(`{op.path_regex_source}`)}},'
        for op in operations
    )
    return f"""// Code generated by scripts/generate_public_operations.py from openapi.yaml. DO NOT EDIT.

package openapi

import (
\t"regexp"
\t"strings"
)

// PublicOperation represents a single public (unauthenticated) OpenAPI operation.
type PublicOperation struct {{
\tMethod      string
\tPathPattern *regexp.Regexp
}}

// PublicOperations lists the operations that may be called without CDP API credentials,
// per the `security` field in openapi.yaml.
var PublicOperations = []PublicOperation{{
{entries}
}}

// IsPublicOperation returns true if the given HTTP method and path correspond to a public
// (unauthenticated) operation, as declared by the `security` field in openapi.yaml.
func IsPublicOperation(method, path string) bool {{
\tnormalizedMethod := strings.ToUpper(method)
\tfor _, operation := range PublicOperations {{
\t\tif operation.Method == normalizedMethod && operation.PathPattern.MatchString(path) {{
\t\t\treturn true
\t\t}}
\t}}
\treturn false
}}
"""


def write_file(path: Path, contents: str) -> None:
    """Write `contents` to `path`, creating parent directories as needed."""
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(contents)
    print(f"Wrote {path.relative_to(REPO_ROOT)}")


RENDERERS = {
    "typescript": (TS_OUTPUT_PATH, render_typescript),
    "python": (PYTHON_OUTPUT_PATH, render_python),
    "go": (GO_OUTPUT_PATH, render_go),
}


def main() -> None:
    """Load public operations from the OpenAPI spec and regenerate the requested outputs."""
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "spec",
        nargs="?",
        default=str(DEFAULT_SPEC_PATH),
        help="Path to the OpenAPI spec (defaults to the repo-root openapi.yaml).",
    )
    parser.add_argument(
        "--language",
        choices=[*RENDERERS, "all"],
        default="all",
        help="Which language's lookup table to regenerate (defaults to all).",
    )
    args = parser.parse_args()

    spec_path = Path(args.spec)
    operations = load_public_operations(spec_path)

    print(f"Found {len(operations)} public operation(s) in {spec_path.name}:")
    for op in operations:
        print(f"  {op.method} {op.path}")

    languages = list(RENDERERS) if args.language == "all" else [args.language]
    for language in languages:
        output_path, render = RENDERERS[language]
        write_file(output_path, render(operations))


if __name__ == "__main__":
    main()
